<h2>Welcome</h2>
            
            
<p>
    Welcome to our simple country / region / national animal database.  You can use this website to quickly find out where a country is (ie: the region / sub region).  
</p>

<p>
    Almost two thirds of the entries include a 'quality of life' score.  The highest scoring country according to our data is 76 (Australia). You can search for countries using 'quality of life'.  The descriptors are:
</p>

<ul>
    <li>unknown (no quality of life score given)</li>
    <li>yikes (in the bottom 10% of countries, score of less than 41)</li>
    <li>very low (in the bottom 25%, score between 41 and 47)</li>
    <li>low (in the bottom 40%, score of between 48 and 51)</li>
    <li>average (between 40 and 60%, score between 52 and 57)</li>
    <li>good (between 61 and 80%, score between 58 and 66)</li>
    <li>great (between 81 and 90%, score between 67 and 69)</li>
    <li>top 10! (in the top 10 countries, score of 70 or more)</li>
</ul>

<p>
    In addition, you can use this database to look up a country's national animal.  Interestingly, some of these animals are mythical creatures!
</p>
<p>
    We hope you have fun exploring this database.
</p>